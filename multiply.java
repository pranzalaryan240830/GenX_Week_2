package week_2;

public class multiply {
	// Multiplication of 2 decimal numbers
		static double Multiply(double a, double b)
	    {
	        return a * b;
	    }

	// Multiplication of 1 whole and 1 decimal number
		static double Multiply(int a, double b)
		{
		    return a * b;
		}	
	// Multiplication of 2 whole numbers
		static int Multiply(int a, int b)
	    {
	        return a * b;
	    }
	 
	// Multiplication of 3 whole numbers
	    static int Multiply(int a, int b, int c)
	    {
	         return a * b * c;
	    }

}
